#include <stdio.h>

int main() {
    // Keywords are reserved words in C

    int number = 10;   // 'int' is a keyword
    float price = 20.5; // 'float' is a keyword
    char letter = 'A'; // 'char' is a keyword
    return 0;          // 'return' is a keyword

    // Other keywords include if, else, while, for, switch, case, etc.

    return 0;
}
