#include <stdio.h>

int main() {
    // Integer Data Type
    int number = 10;

    // Floating Point Data Type
    float price = 99.99;

    // Character Data Type
    char letter = 'C';

    // Double Data Type
    double bigNumber = 12345.6789;

    printf("Integer: %d\n", number);
    printf("Float: %.2f\n", price);
    printf("Character: %c\n", letter);
    printf("Double: %lf\n", bigNumber);

    return 0;
}
